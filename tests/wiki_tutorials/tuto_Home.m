% * The processing of acoustic data in _CoFFee_ is often highly simplified. For example, (to date) it does not account for vessel motion (roll, pitch, heave), it does not re-raybend the soundings, and it does not radiometrically correct the backscatter data. It is also not optimised for speed. In other words, it does not process data to the level of quality and speed of a professional hydrographic software. If you need professional results, you will need professional software. As detailed in the code's [LICENSE file](https://github.com/alexschimel/CoFFee/blob/master/LICENSE), the authors are not liable for any issues arising from using this software. 
